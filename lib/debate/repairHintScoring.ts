/**
 * Counterfactual scoring for turn validation repair hints.
 *
 * For each repair hint, generates a counterfactual improved claim via LLM,
 * substitutes it into the argument network, recomputes QBAF strengths,
 * and measures the strength delta. Hints that produce larger deltas are
 * more relevant — they identified genuine vulnerabilities.
 *
 * The QBAF recomputation is entirely local (no API calls). The only
 * LLM call is generating the counterfactual claim text.
 */

import type { QbafNode, QbafEdge } from './qbaf.js';
import { computeQbafStrengths } from './qbaf.js';

// ── Types ─────────────────────────────────────────────────

export interface RepairHintScore {
  /** The original repair hint text */
  hint: string;
  /** The original claim text that the hint targets */
  original_claim: string;
  /** The AN node ID of the claim (if identifiable) */
  claim_id: string | null;
  /** The counterfactual improved claim text (generated by LLM) */
  counterfactual_claim: string;
  /** Original QBAF strength of the claim */
  original_strength: number;
  /** Counterfactual QBAF strength after applying the fix */
  counterfactual_strength: number;
  /** Strength delta: positive = hint improved the claim */
  delta: number;
  /** Relevance classification based on delta */
  relevance: 'high' | 'medium' | 'low' | 'none';
}

export interface RepairHintScoringResult {
  /** Per-hint scores */
  scores: RepairHintScore[];
  /** Mean delta across all hints */
  mean_delta: number;
  /** Fraction of hints that improved claim strength */
  actionable_rate: number;
}

export interface RepairHintScoringInput {
  /** The debate statement text */
  statement: string;
  /** The speaker's AN claim nodes from this turn */
  speakerClaims: { id: string; text: string; base_strength: number }[];
  /** All AN nodes (for QBAF context) */
  allNodes: QbafNode[];
  /** All AN edges (for QBAF context) */
  allEdges: QbafEdge[];
  /** The repair hints to score */
  hints: string[];
  /** LLM call function: takes a prompt, returns generated text */
  generate: (prompt: string) => Promise<string>;
}

// ── Counterfactual prompt ─────────────────────────────────

function buildCounterfactualPrompt(
  statement: string,
  claim: string,
  hint: string,
): string {
  return `You are an argument improvement assistant. A debate judge identified a weakness in a debater's argument. Your task is to rewrite ONLY the specific claim to fix the identified weakness, keeping everything else the same.

ORIGINAL STATEMENT (for context):
${statement.slice(0, 1500)}

SPECIFIC CLAIM TO IMPROVE:
"${claim}"

WEAKNESS IDENTIFIED BY JUDGE:
"${hint}"

Rewrite the claim to directly address the weakness. Be specific — add the missing evidence, fix the logical gap, or address the unhandled counterpoint. Keep the same argumentative direction but make it stronger.

Respond with ONLY the improved claim text (1-3 sentences, no JSON, no explanation).`;
}

// ── Hint-to-claim matching ────────────────────────────────

/**
 * Match a repair hint to the most relevant claim from this turn.
 * Uses keyword overlap between the hint and each claim.
 */
function matchHintToClaim(
  hint: string,
  claims: { id: string; text: string }[],
): { id: string; text: string } | null {
  if (claims.length === 0) return null;
  if (claims.length === 1) return claims[0];

  const hintWords = new Set(
    hint.toLowerCase().split(/\W+/).filter(w => w.length > 3),
  );

  let bestMatch = claims[0];
  let bestOverlap = 0;

  for (const claim of claims) {
    const claimWords = claim.text.toLowerCase().split(/\W+/).filter(w => w.length > 3);
    const overlap = claimWords.filter(w => hintWords.has(w)).length;
    if (overlap > bestOverlap) {
      bestOverlap = overlap;
      bestMatch = claim;
    }
  }

  return bestMatch;
}

// ── Core scoring function ─────────────────────────────────

/**
 * Score repair hints by counterfactual QBAF strength delta.
 *
 * For each hint:
 * 1. Match it to the most relevant claim from this turn
 * 2. Generate a counterfactual improved version (1 LLM call)
 * 3. Substitute the improved claim into the AN (local)
 * 4. Recompute QBAF strengths (local, deterministic)
 * 5. Measure the strength delta
 */
export async function scoreRepairHints(
  input: RepairHintScoringInput,
): Promise<RepairHintScoringResult> {
  // Compute baseline QBAF strengths
  const baseline = computeQbafStrengths(input.allNodes, input.allEdges);

  const scores: RepairHintScore[] = [];

  for (const hint of input.hints) {
    // Match hint to a claim
    const matched = matchHintToClaim(hint, input.speakerClaims);
    if (!matched) {
      scores.push({
        hint,
        original_claim: '',
        claim_id: null,
        counterfactual_claim: '',
        original_strength: 0,
        counterfactual_strength: 0,
        delta: 0,
        relevance: 'none',
      });
      continue;
    }

    const originalStrength = baseline.strengths.get(matched.id) ?? 0;

    // Generate counterfactual claim via LLM
    const prompt = buildCounterfactualPrompt(input.statement, matched.text, hint);
    let counterfactualText: string;
    try {
      counterfactualText = await input.generate(prompt);
      // Clean up — remove quotes, markdown, etc.
      counterfactualText = counterfactualText.replace(/^["'`]+|["'`]+$/g, '').trim();
    } catch {
      // LLM call failed — skip this hint
      scores.push({
        hint,
        original_claim: matched.text,
        claim_id: matched.id,
        counterfactual_claim: '(generation failed)',
        original_strength: originalStrength,
        counterfactual_strength: originalStrength,
        delta: 0,
        relevance: 'none',
      });
      continue;
    }

    // Substitute improved claim into AN and recompute QBAF
    // The substitution changes the base_strength of the claim node.
    // A better-evidenced claim should have higher base strength.
    // Heuristic: if the counterfactual is longer and more specific, bump base strength.
    const specificityBoost = estimateSpecificityGain(matched.text, counterfactualText);
    const counterfactualNodes = input.allNodes.map(n =>
      n.id === matched.id
        ? { ...n, base_strength: Math.min(1, n.base_strength + specificityBoost) }
        : n,
    );

    const counterfactual = computeQbafStrengths(counterfactualNodes, input.allEdges);
    const counterfactualStrength = counterfactual.strengths.get(matched.id) ?? 0;
    const delta = counterfactualStrength - originalStrength;

    scores.push({
      hint,
      original_claim: matched.text,
      claim_id: matched.id,
      counterfactual_claim: counterfactualText,
      original_strength: originalStrength,
      counterfactual_strength: counterfactualStrength,
      delta,
      relevance: classifyRelevance(delta),
    });
  }

  const deltas = scores.map(s => s.delta);
  const meanDelta = deltas.length > 0
    ? deltas.reduce((a, b) => a + b, 0) / deltas.length
    : 0;
  const actionableRate = deltas.length > 0
    ? deltas.filter(d => d > 0.01).length / deltas.length
    : 0;

  return { scores, mean_delta: meanDelta, actionable_rate: actionableRate };
}

// ── Specificity estimation ────────────────────────────────

/**
 * Estimate how much more specific/evidenced the counterfactual claim is
 * compared to the original. Returns a base_strength boost [0, 0.2].
 *
 * Heuristics:
 * - Contains numbers/percentages the original didn't → +0.05
 * - Contains named entities the original didn't → +0.05
 * - Contains citations/sources the original didn't → +0.05
 * - Longer with more content words → +0.05
 */
function estimateSpecificityGain(original: string, counterfactual: string): number {
  let boost = 0;

  const origHasNumber = /\d/.test(original);
  const cfHasNumber = /\d/.test(counterfactual);
  if (cfHasNumber && !origHasNumber) boost += 0.05;

  const origHasEntity = /[A-Z][a-z]+\s[A-Z][a-z]+/.test(original);
  const cfHasEntity = /[A-Z][a-z]+\s[A-Z][a-z]+/.test(counterfactual);
  if (cfHasEntity && !origHasEntity) boost += 0.05;

  const citationPattern = /(?:study|research|data|evidence|report|survey|analysis|according to)/i;
  const origHasCitation = citationPattern.test(original);
  const cfHasCitation = citationPattern.test(counterfactual);
  if (cfHasCitation && !origHasCitation) boost += 0.05;

  const origWords = original.split(/\s+/).length;
  const cfWords = counterfactual.split(/\s+/).length;
  if (cfWords > origWords * 1.3 && cfWords <= origWords * 3) boost += 0.05;

  return Math.min(0.2, boost);
}

// ── Relevance classification ──────────────────────────────

function classifyRelevance(delta: number): RepairHintScore['relevance'] {
  if (delta >= 0.10) return 'high';
  if (delta >= 0.05) return 'medium';
  if (delta > 0.01) return 'low';
  return 'none';
}
